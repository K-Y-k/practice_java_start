package variable;

public class Var5 {
    public static void main(String[] args) {
        // 1. 변수 선언
        int a;

        // 2. 변수 초기화
        a = 1;
        System.out.println(a);


        // 변수 선언과 초기화를 한번에
        int b = 2;
        System.out.println(b);


        // 여러 변수 선언과 초기화를 한번에
        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);
    }
}
